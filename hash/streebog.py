"""
streebog.py — хэш-функция ГОСТ Р 34.11-2012 («Стрибог»).

Реализует алгоритмы:
  - базовые преобразования S, P, L, X, LPS
  - функцию сжатия g_N
  - процедуру вычисления хэша streebog()

Соглашение об endianness (единое для всего проекта):
  Внутреннее представление состояния — bytes длиной 64.
  state[0] — GOST-байт a_0 (младший, «правый» в записи стандарта).
  state[63] — GOST-байт a_63 (старший, «левый»).
  Display hex: state[::-1].hex()  ← соответствует тексту стандарта.
  Входные данные (message) — обычный поток байт (первый байт = MSB).
  Выходной хэш — тоже в display-порядке (старший байт первым).
"""

from hash.tables import PI, TAU, MATRIX_A, C_BYTES, IV_512, IV_256


# ---------------------------------------------------------------------------
# Вспомогательные операции над 64-байтовыми блоками
# ---------------------------------------------------------------------------

def _xor(a: bytes, b: bytes) -> bytes:
    """Побайтовый XOR двух блоков длиной 64."""
    return bytes(x ^ y for x, y in zip(a, b))


def _add_mod_512(a: bytes, b: bytes) -> bytes:
    """Сложение двух 512-битных значений по модулю 2^512.

    Используется для накопителей N (счетчик длины) и Σ (контрольная сумма).
    Оба операнда — bytes длиной 64 в little-endian (state[0] — младший байт).
    """
    s = int.from_bytes(a, 'little') + int.from_bytes(b, 'little')
    return (s & ((1 << 512) - 1)).to_bytes(64, 'little')


# ---------------------------------------------------------------------------
# Раздел 6: базовые преобразования
# ---------------------------------------------------------------------------

def _S(state: bytes) -> bytes:
    """Нелинейное преобразование S: побайтовое применение π (формула 4).

    S(a_63 || ... || a_0) = π(a_63) || ... || π(a_0)
    Поскольку внутренний буфер хранит байты в порядке [a_0, ..., a_63],
    применяем PI к каждому байту позиционно — индекс в буфере
    соответствует GOST-индексу, поэтому просто map(PI, state).
    """
    return bytes(PI[b] for b in state)


def _P(state: bytes) -> bytes:
    """Перестановка байт P: переставляем согласно τ (формула 5).

    P(a_63 || ... || a_0) = a_{τ(63)} || ... || a_{τ(0)}
    Во внутреннем буфере байт с GOST-индексом i лежит в state[i],
    поэтому новый байт на позиции i = state[TAU[i]].
    """
    return bytes(state[TAU[i]] for i in range(64))


def _L(state: bytes) -> bytes:
    """Линейное преобразование L: умножение на матрицу A над GF(2) (формула 6).

    L применяется к восьми 64-битным подвекторам независимо.
    Для каждого 64-битного слова w:
      out = XOR{ MATRIX_A[63-k]  для каждого бита k, где (w >> k) & 1 }

    Индекс 63-k: бит k слова w соответствует GOST-биту k,
    который умножается на строку матрицы с номером k.
    MATRIX_A хранит строки в порядке [строка_63, строка_62, ..., строка_0],
    поэтому строка с GOST-номером k = MATRIX_A[63 - k].

    Слово хранится в little-endian (state[i*8] — младший байт слова i),
    поэтому int.from_bytes(chunk, 'little') дает числовое значение,
    где бит 0 соответствует GOST-биту 0 данного подвектора.
    """
    result = bytearray(64)
    for i in range(8):
        chunk = state[i * 8: (i + 1) * 8]
        word = int.from_bytes(chunk, 'little')
        out = 0
        while word:
            # bit_length() - 1 дает индекс старшего установленного бита
            k = word.bit_length() - 1
            out ^= MATRIX_A[63 - k]
            word ^= (1 << k)          # сбрасываем обработанный бит
        result[i * 8: (i + 1) * 8] = out.to_bytes(8, 'little')
    return bytes(result)


def _X(key: bytes, state: bytes) -> bytes:
    """Сложение с ключом X[k](a) = k ⊕ a (формула 3)."""
    return _xor(key, state)


def _LPS(state: bytes) -> bytes:
    """Композиция L(P(S(state))) — основной строительный блок."""
    return _L(_P(_S(state)))


# ---------------------------------------------------------------------------
# Раздел 7: функция сжатия g_N
# ---------------------------------------------------------------------------

def _E(K: bytes, m: bytes) -> bytes:
    """Внутренний блочный шифр E(K, m) — 12 раундов.

    E(K, m) = X[K_13] ∘ LPS ∘ X[K_12] ∘ ... ∘ LPS ∘ X[K_1](m)

    Ключи K_i вычисляются по схеме:
      K_1 = K
      K_{i+1} = LPS(K_i ⊕ C_i),  i = 1..12
    """
    state = _X(K, m)       # X[K_1](m)
    Ki = K
    for i in range(12):
        state = _LPS(state)                  # LPS
        Ki = _LPS(_X(Ki, C_BYTES[i]))        # следующий раундовый ключ
        state = _X(Ki, state)                # X[K_{i+2}]
    return state


def _g_N(N: bytes, h: bytes, m: bytes) -> bytes:
    """Функция сжатия g_N(h, m) = E(LPS(h ⊕ N), m) ⊕ h ⊕ m (формула 8)."""
    K = _LPS(_X(h, N))
    t = _E(K, m)
    return _xor(_xor(t, h), m)


# ---------------------------------------------------------------------------
# Раздел 8: процедура вычисления хэша
# ---------------------------------------------------------------------------

def _make_padding(tail: bytes) -> bytes:
    """Дополнение неполного блока (шаг 3.1 раздела 8).

    Формирует 64-байтовый блок: 0^{511-|tail|} || 1 || tail
    во внутреннем формате проекта (little-endian).

    tail — остаток сообщения длиной 0..63 байта в обычном (display) порядке.

    В ГОСТе: m := 0^{511-|M|} || 1 || M
    В display это выглядит как: [01] [байты M слева направо]
    Во внутреннем буфере: [байты M в обратном порядке] [0x01] [нули до конца]
    """
    buf = bytearray(64)
    # tail в display-порядке → кладем в буфер в обратном порядке
    for i, byte in enumerate(reversed(tail)):
        buf[i] = byte
    buf[len(tail)] = 0x01    # бит дополнения
    return bytes(buf)


def streebog(message: bytes, digest_size: int = 256) -> bytes:
    """Вычисляет хэш сообщения по ГОСТ Р 34.11-2012.

    Args:
        message:     произвольная последовательность байт.
        digest_size: длина хэша в битах — 256 или 512.

    Returns:
        Хэш в display-порядке (старший байт первым), длиной
        digest_size // 8 байт.

    Алгоритм (разделы 8.1–8.3 ГОСТа с поправкой 2018 г.):

    Этап 1: инициализация
      h := IV, N := 0^512, Σ := 0^512

    Этап 2: полные 512-битные блоки (справа налево по сообщению)
      h := g_N(h, m)
      N := N ⊞ 512         (счетчик обработанных бит)
      Σ := Σ ⊞ m           (контрольная сумма)

    Этап 3: последний (неполный) блок + финализация
      m := padding(tail)
      h := g_N(h, m)
      N := N ⊞ |tail|_bits
      Σ := Σ ⊞ m           (поправка от 2018 года: m, а не |m|)
      h := g_0(h, N)
      h := g_0(h, Σ)
    """
    if digest_size not in (256, 512):
        raise ValueError(f"digest_size должен быть 256 или 512, получено {digest_size}")

    # --- Этап 1: инициализация ---
    h     = IV_256 if digest_size == 256 else IV_512
    N     = b'\x00' * 64
    Sigma = b'\x00' * 64

    # Константа 512 во внутреннем little-endian формате: 512 = 0x200
    N_INCREMENT_512 = (512).to_bytes(64, 'little')

    # --- Этап 2: полные блоки ---
    # Обрабатываем справа налево: берем по 64 байта с конца.
    # «Правый» фрагмент сообщения — это младшие GOST-байты,
    # т.е. во внутреннем буфере они идут первыми (little-endian).
    M = message
    while len(M) >= 64:
        # Берем последние 64 байта в display-порядке →
        # разворачиваем во внутренний буфер (state[0] = младший байт блока)
        m = M[-64:][::-1]
        M = M[:-64]

        h     = _g_N(N, h, m)
        N     = _add_mod_512(N, N_INCREMENT_512)
        Sigma = _add_mod_512(Sigma, m)

    # --- Этап 3: неполный хвост (0..63 байта) ---
    tail_bits = len(M) * 8
    m = _make_padding(M)

    h     = _g_N(N, h, m)
    N     = _add_mod_512(N, tail_bits.to_bytes(64, 'little'))
    Sigma = _add_mod_512(Sigma, m)   # поправка 2018: m (не |m|)

    # --- Финализация ---
    h = _g_N(b'\x00' * 64, h, N)
    h = _g_N(b'\x00' * 64, h, Sigma)

    # Переводим внутренний буфер в display-порядок (MSB первым)
    h_display = h[::-1]

    if digest_size == 512:
        return h_display
    else:
        # MSB-256: старшие 256 бит = первые 32 байта display-вектора
        return h_display[:32]
