"""
gost_signature.py — точка входа в консольную программу 
ЭЦП ГОСТ Р 34.10-2012.

Реализованы:
  - хэш-функция ГОСТ Р 34.11-2012 («Стрибог»), 256 и 512 бит;
  - схема цифровой подписи ГОСТ Р 34.10-2012 на эллиптических кривых;

Поддерживаемые наборы параметров:
  - тестовые наборы из Приложения А ГОСТ Р 34.10–2012;
  - стандартные наборы TC26 из RFC 7836 / RFC 9215;
  - стандартные наборы CryptoPro из RFC 4357.

Использование:

  python gost_signature.py keygen --paramset test-256 --out keys/mykey
      генерирует ключевую пару для указанного набора параметров и сохраняет
      два JSON-файла: keys/mykey.priv (приватный ключ) и keys/mykey.pub
      (публичный ключ).

  python gost_signature.py sign --in doc.txt --key keys/mykey.priv --out doc.sig
      подписывает файл doc.txt приватным ключом. Длина файла подписи —
      64 байта для 256-битной кривой, 128 байт — для 512-битной.

  python gost_signature.py verify --in doc.txt --key keys/mykey.pub --sig doc.sig
      проверяет подпись. Код возврата 0 — подпись верна, 1 — нет.
      
  python gost_signature.py test
      запускает встроенные тесты: контрольные примеры M1/M2 для Стрибога,
      А.1/А.2 для подписи, проверку всех 12 наборов параметров, граничные
      случаи и полный цикл генерация → подпись → проверка.

Структура проекта:

  gost_signature.py    — обертка над cli.main()
  cli.py               — разбор аргументов и обработчики команд
  math_utils.py        — egcd, modinv, mod_pow, тест Миллера — Рабина
  tests.py             — тесты корректности реализации
  ec/                  — операции на эллиптической кривой и наборы параметров
  hash/                — реализация хэш-функции «Стрибог» и ее константы
  keys/                — генерация, сохранение и загрузка ключей; sign/verify

Зависимости:
  стандартная библиотека Python (argparse, json, secrets, pathlib).

Требуется Python 3.10+.
"""

